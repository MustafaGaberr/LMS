export interface LessonSections {
    concept: string;
    importance: string;
    features: string;
}

export interface LessonVideo {
    type: 'youtube' | 'mp4';
    url: string;
}

export interface LessonSectionLabels {
    concept: string;
    importance: string;
    features: string;
}

export interface SubSection {
    id: string;
    label: string;
    content: string;
}

export interface Lesson {
    id: string;
    title: string;
    sections: LessonSections;
    sectionLabels?: LessonSectionLabels;
    subSections?: Record<string, SubSection[]>;
    video: LessonVideo;
    activityInstructions: string;
    /** Full intro message context for the quiz-intro page */
    quizIntroFullContent: string;
    /** Mind map data for the "Importance" tab (or specified mindMapTab) */
    mindMapData?: MindMapNodeData[];
    /** Which tab should show the video? Defaults to 'features' if not specified. */
    videoTab?: 'concept' | 'importance' | 'features';
    /** Which tab should show the mind map? Defaults to 'importance' if not specified. */
    mindMapTab?: 'concept' | 'importance' | 'features';
}

export interface Unit {
    id: string;
    title: string;
    description: string;
    icon: string;
    lessons: Lesson[];
}

export interface Objective {
    id: string;
    title: string;
    details: string;
    subObjectives: string[];
}

export interface Course {
    title: string;
    subject?: string;
    code?: string;
    description: string;
    objectives: Objective[];
    units: Unit[];
}

